# 声明整形变量并赋值
int_var = 999
print(type(int_var))  # <class 'int'>

# 声明布尔类型变量并赋值
bool_var = False
print(type(bool_var))  # <class 'bool'>
print(type(bool_var) == bool)  # True
print(isinstance(bool_var, bool))  # True

